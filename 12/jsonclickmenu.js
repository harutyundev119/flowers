
function appetizers() {
    var x = document.getElementById("appetizers2");
    if (x.style.display !== "none") {
        x.style.display = "none";




        document.getElementById("appetizers1").style.display = "none";
        document.getElementById("appetizers2").style.display = "none";
        document.getElementById("appetizers").style.background = "none";
        document.getElementById("appetizersname").style.color = "#124E3A";


    } else {
        document.getElementById("appetizersname").style.color = "#FFFFFF";
        document.getElementById("appetizers").style.display = "flex";
        document.getElementById("appetizers1").style.display = "flex";
        document.getElementById("appetizers2").style.display = "flex";
        document.getElementById("appetizers").style.background = "url('./sandwich.png')";
    }
}

